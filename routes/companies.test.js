/************************************** POST /companies */


describe("POST /companies", function () {
  const newCompany = {
    handle: "new",
    name: "New",
    logoUrl: "http://new.img",
    description: "DescNew",
    numEmployees: 10,
  };

  test("ok for users", async function () {
    const resp = await request(app)
        .post("/companies")
        .send(newCompany)
        .set("authorization", `Bearer ${u1Token}`);
    expect(resp.statusCode).toEqual(201);
    expect(resp.body).toEqual({
      company: newCompany,
    });
  });

  test("bad request with missing data", async function () {
    const resp = await request(app)
        .post("/companies")
        .send({
          handle: "new",
          numEmployees: 10,
        })
        .set("authorization", `Bearer ${u1Token}`);
    expect(resp.statusCode).toEqual(400);
  });

  test("bad request with invalid data", async function () {
    const resp = await request(app)
        .post("/companies")
        .send({
          ...newCompany,
          logoUrl: "not-a-url",
        })
        .set("authorization", `Bearer ${u1Token}`);
    expect(resp.statusCode).toEqual(400);
  });

  test("works for admin", async function () {
    const resp = await request(app)
        .post("/companies")
        .send(newCompany)
        .set("authorization", `Bearer ${adminToken}`);
    expect(resp.statusCode).toEqual(201);
    expect(resp.body).toEqual({
      company: newCompany,
    });
  });

  test("unauthorized for non-admin", async function () {
    const resp = await request(app)
        .post("/companies")
        .send(newCompany)
        .set("authorization", `Bearer ${u1Token}`);
    expect(resp.statusCode).toEqual(401);
  });

  test("bad request with missing data", async function () {
    const resp = await request(app)
        .post("/companies")
        .send({
          handle: "new",
          numEmployees: 10,
        })
        .set("authorization", `Bearer ${adminToken}`);
    expect(resp.statusCode).toEqual(400);
  });

  test("bad request with invalid data", async function () {
    const resp = await request(app)
        .post("/companies")
        .send({
          ...newCompany,
          logoUrl: "not-a-url",
        })
        .set("authorization", `Bearer ${adminToken}`);
    expect(resp.statusCode).toEqual(400);
  });
});

/************************************** GET /companies */

describe("GET /companies", function () {
  test("works: no filters", async function () {
    const res = await request(app).get("/companies");
    expect(res.statusCode).toBe(200);
    expect(res.body.companies.length).toEqual(3);  // assuming you have 3 companies in test data
  });

  test("works: filter by name", async function () {
    const res = await request(app).get("/companies").query({ name: "C1" });
    expect(res.statusCode).toBe(200);
    expect(res.body.companies).toEqual([
      {
        handle: "c1",
        name: "C1",
        description: "Desc1",
        numEmployees: 1,
        logoUrl: "http://c1.img",
      },
    ]);
  });

  test("works: filter by minEmployees", async function () {
    const res = await request(app).get("/companies").query({ minEmployees: 2 });
    expect(res.statusCode).toBe(200);
    expect(res.body.companies.length).toEqual(2);  // assuming companies C2 and C3 match
  });

  test("works: filter by maxEmployees", async function () {
    const res = await request(app).get("/companies").query({ maxEmployees: 2 });
    expect(res.statusCode).toBe(200);
    expect(res.body.companies.length).toEqual(2);  // assuming companies C1 and C2 match
  });

  test("works: combined filters", async function () {
    const res = await request(app).get("/companies").query({ name: "C", minEmployees: 2, maxEmployees: 3 });
    expect(res.statusCode).toBe(200);
    expect(res.body.companies.length).toEqual(2);  // assuming companies C2 and C3 match
  });

  test("bad request: minEmployees > maxEmployees", async function () {
    const res = await request(app).get("/companies").query({ minEmployees: 10, maxEmployees: 5 });
    expect(res.statusCode).toBe(400);
    expect(res.body.error).toEqual("minEmployees cannot be greater than maxEmployees");
  });

  test("fails: test next() handler", async function () {
    // there's no normal failure event which will cause this route to fail ---
    // thus making it hard to test that the error-handler works with it. This
    // should cause an error, all right :)
    await db.query("DROP TABLE companies CASCADE");
    const resp = await request(app)
        .get("/companies")
        .set("authorization", `Bearer ${u1Token}`);
    expect(resp.statusCode).toEqual(500);
  });
});

